package egovframework.itman.email.service;

public class EgovItmanEmailVO {

	private String ecIdx;
	private String email;
	private String memName;
	private String ecNum;
	private String ecMode;
	private String regDate;

	public String getEcIdx() {
		return ecIdx;
	}

	public void setEcIdx(String ecIdx) {
		this.ecIdx = ecIdx;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMemName() {
		return memName;
	}

	public void setMemName(String memName) {
		this.memName = memName;
	}

	public String getEcNum() {
		return ecNum;
	}

	public void setEcNum(String ecNum) {
		this.ecNum = ecNum;
	}

	public String getEcMode() {
		return ecMode;
	}

	public void setEcMode(String ecMode) {
		this.ecMode = ecMode;
	}

	public String getRegDate() {
		return regDate;
	}

	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}

}
